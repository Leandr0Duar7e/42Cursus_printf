#include <stdio.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)s;
	while (i < n && *str != (unsigned char)c)
	{
		str++;
		i++;
	}
	if (i >= n)
		return (0);
	return (str);
}
